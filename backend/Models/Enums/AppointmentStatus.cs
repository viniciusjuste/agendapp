/// <summary>
/// Represents the status of an appointment.
/// </summary>
public enum AppointmentStatus
{
    /// <summary>
    /// The appointment is pending and awaiting confirmation.
    /// </summary>
    Pending,

    /// <summary>
    /// The appointment has been confirmed.
    /// </summary>
    Confirmed,

    /// <summary>
    /// The appointment has been cancelled.
    /// </summary>
    Canceled
}
